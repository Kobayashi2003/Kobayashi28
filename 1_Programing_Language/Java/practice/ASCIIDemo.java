public class ASCIIDemo {
    public static void main(String[] args) {
        System.out.println('a' + 10); 
        System.out.println('A' + 10);
        System.out.println('0' + 9);
        
        System.out.println(0B10);
        System.out.println(010);
        System.out.println(0X10);
    }
}
