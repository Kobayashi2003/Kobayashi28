DATA SEGMENT
STRING DB 'HELLO WORLD!'
COUNT EQU $-STRING
DATA ENDS

EXTRA SEGMENT
DEST DB COUNT DUP(?)
EXTRA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:EXTRA
BEGIN:
    MOV AX,DATA
    MOV DS,AX
    MOV AX,EXTRA
    MOV ES,AX

    LEA SI,STRING
    LEA DI,DEST
    MOV CX,COUNT
    
AG:
    LODSB
    AND AL,7FH
    JPE NEXT
    OR AL,80H
NEXT:STOSB
    LOOP AG

    MOV AH,4CH
    INT 21H

CODE ENDS
    END BEGIN
