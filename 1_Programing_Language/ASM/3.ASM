DATA SEGMENT
SOURCE DB 1,2,3,5,6,0
COUNT EQU &-SOURCE
DATA ENDS
EXTRA SEGMENT
DEST DB COUNT DUP (0)
EXTRA ENDS
CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,ES:EXTRA
BEGIN:
    MOV AX,DATA
    MOV DS,AX
    MOV CX,COUNT
    LEA SI,SOURCE
    LEA DI,DEST
    REP MOVS DEST,SOURCE
AG:
    MOV AL,[SI]
    MOV ES:[DI],AL
    INC SI
    INC DI
    LOOP AG
    MOV AH,4CH
    INT 21H
CODE ENDS
    END BEGIN